//===================================================================================
BOOL CTransfer_ControlApp::InitApplication()
{
	if (PreInstance())
	{
		AfxMessageBox(_T("이미 Program이 실행 중 입니다."));
		return FALSE;
	}

	return CWinAppEx::InitApplication();
}
//===================================================================================
BOOL CTransfer_ControlApp::PreInstance()
{
	// Mutex 생성. //
	HANDLE hOneInstance = ::CreateMutex(NULL, TRUE, _T("Transfer System(2014. 12. ) by sung-hwan Ahn"));

	// 만약 이미 만들어져 있다면 Instance가 이미 존재함. //
	// 이미 하나의 Instance가 존재하면 프로그램 종료. //
	if (::GetLastError() == ERROR_ALREADY_EXISTS)
		return TRUE;
	if (hOneInstance)
		::ReleaseMutex(hOneInstance);
	return FALSE;
}

//===================================================================================
BOOL CTransfer_ControlApp::DoEvents()
{
	MSG msg;
	while (::PeekMessage(&msg, NULL, 0, 0, PM_NOREMOVE))
	{
		if (!PumpMessage())
		{
			::PostQuitMessage(0);
			return FALSE;
		}
	}
	return TRUE;
}
